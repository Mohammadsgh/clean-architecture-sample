<?php
declare(strict_types=1);

namespace App\ViewModels;

class ViewModel implements ViewModelInterface
{
    /**
     * @var array
     */
    private $data;

    public function __construct(array $data)
    {
        $this->data = $data;
    }

    public function toArray(): array
    {
        return $this->data;
    }
}
